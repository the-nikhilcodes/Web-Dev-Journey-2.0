//Logical Operations

// && -> and
// || -> or
// ! -> reverse

let islogin = true;
let ispaid = false;

console.log(islogin && ispaid); //false
console.log(islogin || ispaid); //true
console.log(!islogin); //false

let idEmailUser = true;
let idEmailAdmin = false;

console.log(idEmailUser && idEmailAdmin); //false